package com.demo.entity;

public class Rating {
	
	private long id;
	private String ratingdetails;
	private String gotrationg;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getRatingdetails() {
		return ratingdetails;
	}
	public void setRatingdetails(String ratingdetails) {
		this.ratingdetails = ratingdetails;
	}
	public String getGotrationg() {
		return gotrationg;
	}
	public void setGotrationg(String gotrationg) {
		this.gotrationg = gotrationg;
	}

}
